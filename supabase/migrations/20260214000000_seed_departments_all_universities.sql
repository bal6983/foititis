-- 4) Insert departments by resolving IDs through joins
insert into public.departments (name, school_id, city_id)
select distinct
  t.department_name,
  s.id,
  c.id
from tmp_dept_seed t
join public.universities u on u.domain = t.university_domain
join public.schools s on s.university_id = u.id and s.name = t.school_name
join public.cities c on c.name = t.city_name
on conflict (school_id, name, city_id) do nothing;

